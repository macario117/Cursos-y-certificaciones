#Este es un programa en donde algunas de las instrucciones se ejecutan de forma repetitiva y secuencial
#El objetivo de este programa es mostrar la cuenta regresiva del numero n
print("Programa con Repeticiones")#Print imprime el titulo del programa
n = 50
while n > 0 : #Mientras n>0 ejecueta las siguientes instrucciones. Inicia la cuenta regresiva
    print(n) #Imprime el valor actual de n
    n = n - 1 #Hace la resta, se actualiza "n" (n-1) y se repite el ciclo hasta que llegue la cuenta a 1 (n>0)
print('Despeque!')
