# Programa que compara una variable y te dice si es mayor que el numero 2
# Empleamos el ciclo for y condicional if

x=3 #La varibale es X=3
if x > 2: #Compara si X es mayor que dos
    print('X Es mayor que 2') 
    print('x Sigue siendo mayor')
print('Hecho con: ',x)
print('---------------------------------')
for i in range(5):    #Creamos un ciclo for para hacer una comparacion, el ciclo termina en i0 y termina en i5
    print(i)
    if i > 2:         #Dentro del ciclo compra la variable i y verifica si es mayor que 2
        print('Mayor que 2')
    print("Hecho con ", i)
    print('---------------------------------')
print('Todo listo')
