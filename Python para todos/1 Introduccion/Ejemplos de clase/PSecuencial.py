#Este es un programa en donde las instrucciones se ejecutan unicamente de forma secuencial.
#El objetivo de este programa es hacer una suma utilizando la expresión matemática x=x+2

print("Programa secuencial") #Print imprime el titulo del programa
x = 2 #Declaramos una constate bajo el nombre “x”, el valor de la constante es 2 y lo guardamos en la memoria principal
print(x) #Imprimimos el valor de la constante que se encuentra en la memoria y cuyo nombre es “x”
x=x+2 #Utilizamos la expresion x=x+2. De la memoria traemos el valor actual de la constante x ( x=2 ), a ese valor le sumamos 2 y al resultado de la operación (x= (2) +2 → x=4) lo volvemos a guardar en la variable X; es decir, se actualiza.
print(x) #Imprimimos el nuevo valor de la constante x (x=4)
