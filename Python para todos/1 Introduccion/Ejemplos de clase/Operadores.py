# Programa que explica el funcionamiento de los operadores

x = 8
if x == 5 :
    print(' X es igual que 5')
if x > 4 :
    print(' X es mayor que 4')
if  x >= 5 :
    print(' X es mayor o igual que 5')
if x < 6 :
    print(' X es menor que 6')
if x <= 5 :
    print(' X es menor o igual que 5')
if x != 6 :
    print(' X no es igual que 6')
