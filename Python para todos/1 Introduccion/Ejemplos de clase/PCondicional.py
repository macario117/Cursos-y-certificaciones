#El objetivo de este programa es comparar la constante “x” y mostrar si el valor de “x” es menor que 10 o mayor que 20
print("Programa condicional") #Print imprime el titulo del programa
x = 50 #Declaramos una constate bajo el nombre “x”, el valor de la constante es 5 y lo guardamos en la memoria principal
